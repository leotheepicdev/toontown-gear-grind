def pickFromFreqList(freqList):
    randNum = random.randint(0, 99)
    count = 0
    index = 0
    level = None
    for f in freqList:
        count = count + f
        if randNum < count:
            level = index
            break
        index = index + 1

    return level

def getActualFromRelativeLevel(name, relLevel):
    data = SuitAttributes[name]
    actualLevel = data['level'] + relLevel
    return actualLevel

def getSuitVitals(name, level = -1):
    data = SuitAttributes[name]
    if level == -1:
        level = pickFromFreqList(data['freq'])
    dict = {}
    dict['level'] = getActualFromRelativeLevel(name, level)
    if dict['level'] == 11:
        level = 0
    dict['hp'] = data['hp'][level]
    dict['def'] = data['def'][level]
    attacks = data['attacks']
    alist = []
    for a in attacks:
        adict = {}
        name = a[0]
        adict['name'] = name
        adict['animName'] = SuitAttacks[name][0]
        adict['hp'] = a[1][level]
        adict['acc'] = a[2][level]
        adict['freq'] = a[3][level]
        adict['group'] = SuitAttacks[name][1]
        alist.append(adict)

    dict['attacks'] = alist
    return dict

def pickSuitAttack(attacks, suitLevel):
    attackId = -1
    randNum = random.randint(0, 99)
    notify.debug('pickSuitAttack: rolled %d' % randNum)
    count = 0

    for name, numbers in attacks.items():
        count = count + numbers[2][suitLevel]
        if randNum < count:
            attackId = SuitAttackNames.index(name)
            notify.debug('picking attack %d' % attackId)
            break
    
    # None of the attacks got picked. Let's randomly choose one.
    if attackId == -1:
        names = list(attacks.keys())
        attackName = random.choice(names)
        attackId = SuitAttackNames.index(attackName)        

    return attackId


def getSuitAttack(suitName, suitLevel, attackName, needAttackInfo=True):
    attackChoices = SuitAttributes[suitName]['attacks']

    adict = {}
    adict['suitName'] = suitName
    adict['id'] = SuitAttackNames.index(attackName)
    adict['animName'] = SuitAttacks[attackName][0]
    if needAttackInfo:
        attack = attackChoices[attackName]
        adict['hp'] = attack[0][suitLevel]
        adict['acc'] = attack[1][suitLevel]
        adict['freq'] = attack[2][suitLevel]
        adict['status'] = {}
        if len(attack) == 4:
            if 'status' in attack[3]:
                status = attack[3]['status']
                adict['status']['atk'] = status['atk']
                adict['status']['dmg'] = status['dmg'][suitLevel]
                adict['status']['rounds'] = status['rounds']
            if 'dmgAdd' in attack[3]:
                adict['dmgAdd'] = attack[3]['dmgAdd'][suitLevel]
    adict['group'] = SuitAttacks[attackName][1]
    
    return adict
             
